#include <iostream>
using namespace std;


int main()
{
    cout << (10 == 10) << "\n";     //true => 1
    cout << (10 > 10) << "\n";      //false => 0
    cout << (10 < 10) << "\n";      //false => 0
    cout << (10 >= 10) << "\n";     //true 10 = 10 => 1
    cout << (10 <= 10) << "\n";     //true => 1
    cout << (10 != 10) << "\n";     //false => 0
    

    return 0;
}